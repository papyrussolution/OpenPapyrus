					push @print_lines, ['VIRTBUTTON ',$bx-$edit_width,$ay,$edit_width,$edit_width,$vb_ctlNo," BS_BITMAP"];
				}
				last SWITCH;
			};
			/^TV_BUTTON/ && do {
				$cluster_length=0;
				($ax,$ay,$bx,$by,$id,$symb,$text,$cmd,$cmd_txt,$options,$hlpctx,$columns)=split /,\s+/,$par;
