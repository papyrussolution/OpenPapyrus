	}
	$_=<>; #BEGIN statement
	$dialog_header=<>; #header statement  
	$did=$dummy_id;

	@print_lines=();
item:  while (<>) {
		/^\s*(\w+)\s(.*)/;
		$cntl=$1;$par=$2;
		$_=$1;
		SWITCH: {
			(push @print_lines,['END']) && last item if /^END/;
			/^TV_INPUTLINE/ && do {
				$cluster_length=0;
				($ax,$ay,$bx,$by,$id)=split /,\s*/,$par;
				$ax*=4;$bx*=4;$ay*=8;$by*=8;
				$_=$par;
