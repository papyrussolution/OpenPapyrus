					$btn_id--;
					($id,$ax,$ay,$bx,$by)=@group;
					push @print_lines, ['CONTROL ',$ax,$ay,$bx-$ax, $edit_width,"$id + ".($btn_id<<12),$name,
						" \"Button\", BS_AUTOCHECKBOX | BS_NOTIFY | WS_TABSTOP"];
