					$btn_step=10;
					foreach $b (@$tmp) {
						down($b,$top->[2]+$dw+$btn_step);
						$btn_step+=10;
					};
				}
				redo enumerated;
			}
			if ($c->[0] =~/^GROUPBOX/) { 
				$j++;
