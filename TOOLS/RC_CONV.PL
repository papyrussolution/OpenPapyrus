	$dialog_name=$1;

	$_count++;
	print PPHELPRC "\n\t$dialog_name,\t$_count";
	print PPHELPH "#define HELP_$dialog_name\t$_count\n";

	if($2 eq 'TV_STATUS' || $2 eq 'TV_BROWSER' || $2 eq 'TV_REPORT' || $2 eq 'PP_RCDATA') {
		while(<>) {
			last if (/(.*)\}(.*)/);
		}
