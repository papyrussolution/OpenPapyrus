			($_, $ax, $ay, $bx, $by, $id, $name, $text) = @$l;
			$ax = int($ax*$smart_coeff_x);
			$ay = int($ay*$smart_coeff_y);
			print 'CONTROL ',$name,', ',$id,', ',$text,', ',
