				$bottom=$i>$j?$e:$c;
				$dw=$top->[0] =~/^COMBOBOX/?$edit_width:$top->[4];
				$old_bottom_y=$bottom->[2];
				down($bottom,$top->[2]+$dw+2);
				$prev_ctl=$i>$j?$i-1:$j-1;
				$prc=@print_lines[$prev_ctl];
				$next_ctl=$i>$j?$i+1:$j+1;
