			/^TV_STATIC/ && do {
				$par=~/\s*(\d*),\s*(\d*),\s*(\d*),\s*(\d*),\s*(.*),\s*(\".*\"),\s*(\".*\"),\s*(\".*\"),\s*(\".*\")/;
				$ax=$1;
				$ay=$2;
				$bx=$3;
				$by=$4;
				$id=$5;
