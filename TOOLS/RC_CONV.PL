				$ax*=4;$bx*=4;$ay*=8;$by*=8;
				if($text =~/(.*)~(.+)~(.*)/) {
					$text=$1.'&'.$2.$3;
				}
				if($id>=$dummy_id) {
					$id=$did++;
				}
